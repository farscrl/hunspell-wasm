// Reads the version hunspell itself declares (native/vendor/hunspell/configure.ac) rather than
// `git describe` on the submodule — that also works from a shallow checkout, where tag refs
// aren't guaranteed to be fetched, and it's the same string upstream's own `--version` reports.
import { readFileSync, writeFileSync } from 'node:fs';

const configureAcUrl = new URL('../native/vendor/hunspell/configure.ac', import.meta.url);
const outUrl = new URL('../src/hunspellVersion.ts', import.meta.url);

const configureAc = readFileSync(configureAcUrl, 'utf8');
const match = configureAc.match(/AC_INIT\(\[hunspell\],\s*\[([^\]]+)\]/);
if (!match) {
  throw new Error(`Could not find hunspell version in ${configureAcUrl}`);
}

const version = match[1];
writeFileSync(
  outUrl,
  `// Generated by \`pnpm run generate:version\` from native/vendor/hunspell/configure.ac. Do not edit.\nexport const HUNSPELL_VERSION = ${JSON.stringify(version)};\n`,
);
console.log(`src/hunspellVersion.ts -> HUNSPELL_VERSION = ${version}`);
